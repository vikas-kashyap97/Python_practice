# Arithmetic Operators
a = 10
b = 3

print("Arithmetic Operators:")
print("a + b  =", a + b)   # 10 + 3 = 13
print("a - b  =", a - b)   # 10 - 3 = 7
print("a * b  =", a * b)   # 10 * 3 = 30
print("a / b  =", a / b)   # 10 / 3 = 3.3333
print("a // b =", a // b)  # 10 // 3 = 3 (Floor division)
print("a % b  =", a % b)   # 10 % 3 = 1 (Remainder)
print("a ** b =", a ** b)  # 10 ** 3 = 1000 (Exponentiation)
print("\n")

# Assignment Operators
x = 5
print("Assignment Operators:")
x += 3  # x = 5 + 3
print("x += 3:", x)  # 8

x -= 2  # x = 8 - 2
print("x -= 2:", x)  # 6

x *= 4  # x = 6 * 4
print("x *= 4:", x)  # 24

x /= 2  # x = 24 / 2
print("x /= 2:", x)  # 12.0

x //= 2  # x = 12.0 // 2
print("x //= 2:", x)  # 6.0

x %= 3  # x = 6.0 % 3
print("x %= 3:", x)  # 0.0

x **= 2  # x = 0.0 ** 2
print("x **= 2:", x)  # 0.0
print("\n")

# Comparison Operators
print("Comparison Operators:")
print("10 == 10:", 10 == 10)  # True
print("10 != 5:", 10 != 5)    # True
print("10 > 5:", 10 > 5)      # True
print("3 < 7:", 3 < 7)        # True
print("8 >= 8:", 8 >= 8)      # True
print("4 <= 6:", 4 <= 6)      # True
print("\n")

# Logical Operators
print("Logical Operators:")
print("(5 > 2) and (10 > 3):", (5 > 2) and (10 > 3))  # True
print("(5 > 2) or (10 < 3):", (5 > 2) or (10 < 3))   # True
print("not (5 > 2):", not (5 > 2))                    # False
